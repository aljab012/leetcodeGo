package main

import "testing"

func Test_numRookCaptures(t *testing.T) {
	type args struct {
		board [][]byte
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{
			name: "Example 1",
			args: args{
				board: [][]byte{
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', 'p', '.', '.', '.', '.'},
					{'.', '.', '.', 'R', '.', '.', '.', 'p'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', 'p', '.', '.', '.', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
				},
			},
			want: 3,
		},
		{
			name: "Example 2",
			args: args{
				board: [][]byte{
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', 'p', 'p', 'p', 'p', 'p', '.', '.'},
					{'.', 'p', 'p', 'B', 'p', 'p', '.', '.'},
					{'.', 'p', 'B', 'R', 'B', 'p', '.', '.'},
					{'.', 'p', 'p', 'B', 'p', 'p', '.', '.'},
					{'.', 'p', 'p', 'p', 'p', 'p', '.', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
				},
			},
			want: 0,
		},
		{
			name: "Example 3",
			args: args{
				board: [][]byte{
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', 'p', '.', '.', '.', '.'},
					{'.', '.', '.', 'p', '.', '.', '.', '.'},
					{'p', 'p', '.', 'R', '.', 'p', 'B', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
					{'.', '.', '.', 'B', '.', '.', '.', '.'},
					{'.', '.', '.', 'p', '.', '.', '.', '.'},
					{'.', '.', '.', '.', '.', '.', '.', '.'},
				},
			},
			want: 3,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := numRookCaptures(tt.args.board); got != tt.want {
				t.Errorf("numRookCaptures() = %v, want %v", got, tt.want)
			}
		})
	}
}
